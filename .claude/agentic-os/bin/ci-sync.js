#!/usr/bin/env node
'use strict';
// ci-sync — bridge EXISTING CI ⇄ the local agent pre-push.
//   1. MIRROR: parse the repo's existing GitHub/GitLab/etc CI, extract its check commands, and write a
//      local runner at .ecosystem/checks/imported-ci.sh — which the `preflight` skill runs before push.
//      So whatever your server CI checks, you now catch locally FIRST (fix before pushing, not after).
//   2. CRITIQUE: compare the existing pipeline against the recommended gate pack and write
//      .ecosystem/ci-critique.md — "what's covered, what's missing, what to add" (better or more).
// Idempotent, zero-dep, writes nothing executable that the repo didn't already run in its own CI.
// Usage: node bin/ci-sync.js <target>
const fs = require('node:fs');
const path = require('node:path');
const { parseExistingCI, critiqueCI } = require('../src/ci-import');
const { verifyGates, augmentSnippets } = require('../src/ci-maintain');

const root = path.resolve(process.argv[2] || '.');
const eco = path.join(root, '.ecosystem');

const existing = parseExistingCI(root);
const crit = critiqueCI(root);
const gates = verifyGates(root, { run: false });  // TEST the gates first (fast static probe)
const augment = augmentSnippets(root);             // merge-ready steps to fill the holes (more robust)

const written = [];
function write(rel, content, mode) {
  const full = path.join(root, rel);
  fs.mkdirSync(path.dirname(full), { recursive: true });
  fs.writeFileSync(full, content);
  if (mode) { try { fs.chmodSync(full, mode); } catch {} }
  written.push(rel);
}

// 1. MIRROR — local runner of the existing CI's check commands (the preflight skill runs .ecosystem/checks/*).
if (existing.checks.length) {
  const body = existing.checks.map(({ command, category }) =>
    `run_check "${category}" ${JSON.stringify(command)}`).join('\n');
  write('.ecosystem/checks/imported-ci.sh', `#!/usr/bin/env bash
# Imported from this repo's existing CI by agentic-os ci-sync — runs the SAME check commands your
# server CI runs, but LOCALLY before push, so you catch failures first. Regenerate: agentic-os ci-sync.
# Source CI files: ${existing.files.join(', ') || '(none)'}
set -uo pipefail
FAILED=()
run_check() { echo "── [$1] $2"; bash -c "$2"; if [ $? -ne 0 ]; then FAILED+=("$1: $2"); fi; }
${body}
if [ \${#FAILED[@]} -ne 0 ]; then echo "❌ imported-CI checks failed:"; printf '   - %s\\n' "\${FAILED[@]}"; exit 1; fi
echo "✅ imported-CI checks passed"
`, 0o755);
}

// 2. CRITIQUE — the "better or more" report.
const md = [
  '# CI critique — existing pipeline vs recommended gate pack',
  '',
  existing.files.length
    ? `Existing CI detected: ${existing.files.map((f) => '`' + f + '`').join(', ')}.`
    : 'No existing CI detected — agentic-os generated a fresh GitHub Actions + GitLab config (see `.github/workflows/` and `.ecosystem/gitlab-ci.agentic-os.yml`).',
  '',
  '## Covered by your existing CI',
  crit.covered.length ? crit.covered.map((g) => `- ✅ ${g}`).join('\n') : '- (none of the recommended gates)',
  '',
  '## Missing — add these (better / more)',
  crit.suggestions.length ? crit.suggestions.map((s) => `- ⚠️ **${s.gate}** — ${s.how}`).join('\n') : '- nothing missing 🎉',
  '',
  crit.extras.length ? `## Extra checks you already run\n${crit.extras.map((e) => `- ${e}`).join('\n')}\n` : '',
  '## Mirrored locally',
  existing.checks.length
    ? `${existing.checks.length} check command(s) imported to \`.ecosystem/checks/imported-ci.sh\` — the \`preflight\` skill runs them before every push, so server-CI failures surface LOCALLY first.`
    : 'No mirror-worthy check commands found in existing CI (or no existing CI). The generated agentic-os gate pack (`.ecosystem/ci-check.sh`) covers lint/typecheck/test/secret-scan from your own tooling.',
  '',
  '## Gate health (tested before trusting)',
  gates.map((g) => `- ${g.status === 'ready' || g.status === 'pass' ? '✅' : g.status === 'skipped' ? '➖' : '⚠️'} **${g.gate}** — ${g.status}${g.command ? ` (\`${g.command}\`)` : ''}`).join('\n'),
  gates.some((g) => ['no-script', 'tool-missing', 'fail'].includes(g.status))
    ? '\n> ⚠️ A gate above is broken (missing tool/script) — fix or remove it so the pre-push gate is trustworthy.' : '',
  '',
  augment.gates.length ? `## Make it more robust — paste these into your pipeline\nGitHub job steps:\n\`\`\`yaml\n${augment.github}\n\`\`\`\nGitLab \`script:\` lines:\n\`\`\`yaml\n${augment.gitlab}\n\`\`\`\n(Or just let \`.github/workflows/agentic-os-checks.yml\` run them — it already does.)` : '',
  '',
  '_Generated by `agentic-os ci-sync`. The local pre-push hook runs `.ecosystem/ci-check.sh`; the `preflight` skill additionally runs `.ecosystem/checks/*` (incl. the imported mirror) + the map-drift critique. CI auto-refreshes when tooling changes (the `.ecosystem/.ci-dirty` loop)._',
].filter((l) => l !== '').join('\n');
write('.ecosystem/ci-critique.md', md + '\n');

console.log(JSON.stringify({
  hasExistingCI: crit.hasExistingCI,
  files: existing.files,
  mirrored: existing.checks.length,
  covered: crit.covered,
  missing: crit.missing,
  written,
}));
